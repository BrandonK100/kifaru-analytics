import type { Player, PlayerStat, RadarPoint } from '@/types/player.types';

function radar(
  tries: number,
  tackles: number,
  carries: number,
  metres: number,
  lineBreaks: number,
  turnovers: number,
  rating: number
): RadarPoint[] {
  return [
    { stat: 'Tries', value: Math.min(100, tries * 15) },
    { stat: 'Tackles', value: Math.min(100, tackles * 2) },
    { stat: 'Carries', value: Math.min(100, carries) },
    { stat: 'Metres', value: Math.min(100, metres / 3) },
    { stat: 'Line Breaks', value: Math.min(100, lineBreaks * 12) },
    { stat: 'Turnovers', value: Math.min(100, turnovers * 8) },
    { stat: 'Rating', value: rating * 10 },
  ];
}

function ps(
  tries: number,
  tackles: number,
  tackleSuccess: number,
  carries: number,
  metres: number,
  lineBreaks: number,
  turnovers: number,
  rating: number
): PlayerStat {
  return {
    tries,
    tackles,
    tackleSuccessRate: tackleSuccess,
    carries,
    metresGained: metres,
    lineBreaks,
    turnoversWon: turnovers,
    rating,
  };
}

